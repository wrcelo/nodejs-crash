const os = require('os');

// Platform
console.log(os.platform());

// Architecture
console.log(os.arch());

// CPU core info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// Uptime
console.log(os.uptime());
